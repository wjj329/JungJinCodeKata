string[] s = Console.ReadLine().Split();
Console.WriteLine(int.Parse(s[0]) + int.Parse(s[1])); // A + B
Console.WriteLine(int.Parse(s[0]) - int.Parse(s[1])); // A - B
Console.WriteLine(int.Parse(s[0]) * int.Parse(s[1])); // A * B
Console.WriteLine(int.Parse(s[0]) / int.Parse(s[1])); // A / B (몫)
Console.WriteLine(int.Parse(s[0]) % int.Parse(s[1])); // A % B (나머지)
